  # 计息方式介绍
  [[TOC]]
  在金融计算器中，计息方式会直接影响利息的计算结果。常见的计息方式包括单利、复利和连续复利，以下是对这些计息方式的详细介绍和示例。

  ## 单利（Simple Interest）

  **单利**是指只根据本金计算利息，每期产生的利息不加入本金一起计算，适用于短期借贷或一些简单的投资工具。

  ### 公式：
  $$
  A = P \\times (1 + r \\times t)
  $$

  - **A**：最终金额（本金 + 利息）
  - **P**：本金
  - **r**：年利率（小数形式）
  - **t**：时间（年）

  ### 示例：
  如果你投资了 **1000 元**，年利率为 **5%**，投资 **3 年**，使用单利计算：

  $$
  A = 1000 \\times (1 + 0.05 \\times 3) = 1000 \\times 1.15 = 1150 元
  $$

  最终金额为 **1150 元**，赚取的利息为 **150 元**。


  ## 复利（Compound Interest）

  **复利**是指每期利息会加入本金一起参与下一期的利息计算，也被称为“利滚利”，适合长期投资。

  #### 公式：
  $$
  A = P \\times (1 + \\frac{r}{n})^{n \\times t}
  $$

  - **A**：最终金额（本金 + 利息）
  - **P**：本金
  - **r**：年利率（小数形式）
  - **n**：每年复利次数
  - **t**：时间（年）

  ### 示例：
  同样投资 **1000 元**，年利率为 **5%**，投资 **3 年**，每年复利一次：

  $$
  A = 1000 \\times (1 + \\frac{0.05}{1})^{1 \\times 3} = 1000 \\times 1.157625 = 1157.63 元
  $$

  最终金额为 **1157.63 元**，赚取的利息为 **157.63 元**。相比单利，复利的利息多了 **7.63 元**。

  如果每年复利 **4 次**，则：

  $$
  A = 1000 \\times (1 + \\frac{0.05}{4})^{4 \\times 3} = 1000 \\times 1.1611 = 1161.1 元
  $$

  复利次数越多，赚取的利息越多。


  ## 连续复利（Continuous Compounding）

  **连续复利**是复利的极限情况，利息持续不断地计算，也就是每时每刻都在复利。适合非常精确的利息计算。

  ### 公式：
  $$
  A = P \\times e^{r \\times t}
  $$

  - **A**：最终金额（本金 + 利息）
  - **P**：本金
  - **r**：年利率（小数形式）
  - **t**：时间（年）
  - **e**：自然常数，约等于 **2.71828**

  ### 示例：
  如果投资 **1000 元**，年利率为 **5%**，投资 **3 年**，使用连续复利计算：

  $$
  A = 1000 \\times e^{0.05 \\times 3} = 1000 \\times e^{0.15} \\approx 1000 \\times 1.161834 = 1161.83 元
  $$

  最终金额为 **1161.83 元**，赚取的利息为 **161.83 元**，相比普通复利略高。


