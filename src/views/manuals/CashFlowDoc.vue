  # 现金流概念
  [[TOC]]
## 单利（Simple Interest）

**单利** 是指利息仅根据最初的本金计算的方式，而不考虑在时间内产生的利息。公式为：

\`\`\`
I = P * r * t
\`\`\`

- \`I\`：利息
- \`P\`：本金
- \`r\`：利率
- \`t\`：时间

**示例**：如果你以年利率 5% 存入 1000 元，并存 2 年，利息为：

\`\`\`
I = 1000 * 0.05 * 2 = 100
\`\`\`

---

## 复利（Compound Interest）

**复利** 是指不仅对本金计算利息，还对前期积累的利息再计算利息。复利的计算公式为：

\`\`\`
A = P * (1 + r/n)^{n*t}
\`\`\`

- \`A\`：最终金额
- \`P\`：本金
- \`r\`：年利率
- \`n\`：一年内的复利次数
- \`t\`：时间

**示例**：如果本金 1000 元，年利率为 5%，每年复利一次，存两年后的总金额为：

\`\`\`
A = 1000 * (1 + 0.05/1)^{1*2} = 1102.5
\`\`\`

---

## 连续复利（Continuous Compounding）

**连续复利** 是复利的一种极端情况，指在任何时刻都在进行复利计算。连续复利的公式为：

\`\`\`
A = P * e^{r*t}
\`\`\`

- \`A\`：最终金额
- \`P\`：本金
- \`e\`：自然对数的底数（约等于 2.71828）
- \`r\`：年利率
- \`t\`：时间

**示例**：本金为 1000 元，年利率为 5%，两年后的总金额为：

\`\`\`
A = 1000 * e^{0.05*2} = 1105.17
\`\`\`

---

## 净现值（NPV，Net Present Value）

**净现值（NPV）** 是指一项投资未来所有现金流的现值之和减去初始投资额。公式为：

\`\`\`
NPV = \\sum_{t=0}^{T} \\frac{C_t}{(1 + r)^t}
\`\`\`

- \`C_t\`：第 \`t\` 期的现金流
- \`r\`：折现率
- \`t\`：时间（期数）
- \`T\`：总期数

**示例**：假设一项投资有以下现金流：

| 年份 | 现金流 |
|------|--------|
| 0    | -1000  |
| 1    | 500    |
| 2    | 600    |
| 3    | 700    |

折现率为 10%，则净现值为：

\`\`\`
NPV = -1000 + \\frac{500}{(1 + 0.1)^1} + \\frac{600}{(1 + 0.1)^2} + \\frac{700}{(1 + 0.1)^3} = 326.07
\`\`\`

---

## 内部收益率（IRR，Internal Rate of Return）

**内部收益率（IRR）** 是使得投资项目的净现值（NPV）等于零时的折现率。换句话说，IRR 是项目投资回报率的一个衡量指标。公式为：

\`\`\`
NPV = \\sum_{t=0}^{T} \\frac{C_t}{(1 + IRR)^t} = 0
\`\`\`

计算 IRR 一般通过迭代算法解决，因为没有简单的解析解。

**示例**：假设有以下现金流：

| 年份 | 现金流 |
|------|--------|
| 0    | -1000  |
| 1    | 500    |
| 2    | 600    |
| 3    | 700    |

通过迭代计算可以得出内部收益率为约 21.43%。

---

## 现金流时间线

在评估一个项目的投资价值时，通常会绘制一个现金流时间线来显示每个时点的现金流变化情况：

\`\`\`
年份  0    1     2     3
现金流 -1000 +500  +600  +700
\`\`\`

---

## 总结

- **单利**：利息只基于本金计算。
- **复利**：利息基于本金和之前的利息计算。
- **连续复利**：极端情况下的复利计算。
- **NPV**：考虑时间价值后所有现金流的现值之和。
- **IRR**：项目的回报率，NPV 为 0 时的折现率。
